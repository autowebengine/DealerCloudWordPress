// Safe Input Retrieval Class
/*************************************************************************************************/

class InputHelper {

/*************************************************************************************************/

    public static function get($array, $key, $default = "") {
        if(isset($array[$key]) && trim($array[$key]) != '') {
            return $array[$key];
        } else {
            return $default;
        }
    }

/*************************************************************************************************/

    public static function getSecure($array, $key) {
        // TODO
        return get($array, $key);
    }

/*************************************************************************************************/

}

