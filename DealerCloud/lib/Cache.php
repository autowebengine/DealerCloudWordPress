<?php
require_once("Session.php");

/*************************************************************************************************/
// Data Cache Class
/*************************************************************************************************/

class Cache {

/*************************************************************************************************/

    public static function hasMakes() {
        return Session::get("awecache_makes") != null;
    }

/*************************************************************************************************/

    public static function getMakes() {
        return Session::get("awecache_makes");
    }

/*************************************************************************************************/

    public static function setMakes($makes) {
        Session::set("awecache_makes", $makes);
    }
 
/*************************************************************************************************/

    public static function hasModels() {
        return Session::get("awecache_models") != null;
    }

/*************************************************************************************************/

    public static function getModels() {
        return Session::get("awecache_models");
    }

/*************************************************************************************************/

    public static function setModels($models) {
        Session::set("awecache_models", $models);
    }

/*************************************************************************************************/

}

/*************************************************************************************************/
?>
